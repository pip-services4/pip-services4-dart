/// General syntax errors.
class MustacheErrorCode {
  /// The unknown
  static const Unknown = 'UNKNOWN';

  /// The internal error
  static const Internal = 'INTERNAL';

  /// The unexpected end.
  static const UnexpectedEnd = 'UNEXPECTED_END';

  /// The error near
  static const ErrorNear = 'ERROR_NEAR';

  /// The error at
  static const ErrorAt = 'ERROR_AT';

  /// The unexpected symbol
  static const UnexpectedSymbol = 'UNEXPECTED_SYMBOL';

  /// The mismatched brackets
  static const MismatchedBrackets = 'MISTMATCHED_BRACKETS';

  /// The missing variable
  static const MissingVariable = 'MISSING_VARIABLE';

  /// Not closed section
  static const NotClosedSection = 'NOT_CLOSED_SECTION';

  /// Unexpected section end
  static const UnexpectedSectionEnd = 'UNEXPECTED_SECTION_END';
}
