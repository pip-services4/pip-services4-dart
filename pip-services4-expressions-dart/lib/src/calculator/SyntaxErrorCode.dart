///  General syntax errors.
class SyntaxErrorCode {
  /// The unknown
  static final String Unknown = 'UNKNOWN';

  /// The internal error
  static final String Internal = 'INTERNAL';

  /// The unexpected end.
  static final String UnexpectedEnd = 'UNEXPECTED_END';

  /// The error near
  static final String ErrorNear = 'ERROR_NEAR';

  /// The error at
  static final String ErrorAt = 'ERROR_AT';

  /// The unknown symbol
  static final String UnknownSymbol = 'UNKNOWN_SYMBOL';

  /// The missed close parenthesis
  static final String MissedCloseParenthesis = 'MISSED_CLOSE_PARENTHESIS';

  /// The missed close square bracket
  static final String MissedCloseSquareBracket = 'MISSED_CLOSE_SQUARE_BRACKET';
}
